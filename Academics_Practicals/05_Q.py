# 5) Write a python program to make use of following functions of math module: exp(), floor(),factorial(), sqrt(), pow(), cos()

# Solution:
import math

# 1) math.exp(): Returns E raised to the power of x

print(math.exp(2))
# Output: 7.3

print(math.exp(-2))
# Output: 0.1

# 2) math.floor(): Rounds a number down to the nearest integer

print(math.floor(1.9))
# Output: 1

print(math.floor(2.4))
# Output: 2


# 3) math.factorial(): Returns the factorial of a number

print(math.factorial(3))
# Output: 6
print(math.factorial(4))
# Output: 24


# 4) math.sqrt(): Returns the square root of a number

print (math.sqrt(9))
# Output: 3
print (math.sqrt(25))
# Output: 5


# 5) math.pow(): Returns the value of x to the power of y

print(math.pow(9, 3))
# Output: 729


# 6) math.cos(): Returns the cosine of a number

print (math.cos(-1.23))
print (math.cos(10))